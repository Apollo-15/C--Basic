        public void Draw(int remainingAttempts)
        {
            int index = stages.Length - remainingAttempts - 1;

            if (index < 0)
            {
                index = 0;
            }
            if (index >= stages.Length)
            {
                index = stages.Length - 1;
            }
            
            System.Console.WriteLine(stages[index]);
        }
